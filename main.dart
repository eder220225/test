          padding: const EdgeInsets.all(20),
          child: Text('You have '
              '${appState.Testobjekt.length} Dislikes:'),
        ),
        for (var pair in appState.Testobjekt)
          ListTile(
            leading: Icon(Icons.favorite),
            title: Text(pair.asLowerCase),
          ),
      ],
    );
  }
}
